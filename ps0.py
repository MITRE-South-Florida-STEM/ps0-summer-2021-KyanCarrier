import math
# 1. Ask the user to enter a number "x"
print("type a number for x")
x = int(input())

# 2. Ask the user to enter a number "y"
print("type a second number for y")
y = int(input())

print("Enter number x: ", end="")
print(x)
print("Enter number y: ", end="")
print(y)

# 3. Prints out the number "x" raised to the power "y"
print("x**y is: ", end="")
print(x**y)

